package week2;

class MultiArray1{
	public static void main(String args[]){

		//declaring and initializing 2D array
		int arr[][]={{1,2,3,4},{2,4,5,7},{4,4,5,9}, {11,9,8,5}};

		//printing 2D array
		for(int i=0;i<4;i++){
			for(int j=0;j<4;j++){
				System.out.print(arr[i][j]+" ");
 }
			System.out.println();
}

}}
