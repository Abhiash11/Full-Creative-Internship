package week3;

public class ArithmeticOperator
{
    public static void main (String[]args)
    {
        int A = 35;
        int B = 10;
        System.out.println (A + B);
        System.out.println (A - B);
        System.out.println (A * B);
        System.out.println (A / B);
        System.out.println (A % B);
    }
}