package week3;

public class BreakExample2 {  
public static void main(String[] args) {  
    //while loop  
    int i=1;  
    while(i<=10){  
        if(i==5){    
            i++;  
            break;			//it will break the loop  
        }  
        System.out.println(i);  
        i++;  
    }  
}  
}  